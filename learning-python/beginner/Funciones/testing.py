def suma(numero_1, numero_2):
    """2
    La función suma 2 números entenros. 

    Args: 
    numero_1 (int)
    numero_2 (int)

    Se retorna la suma de los parámetros.

    Simular como hacer una prueba en la consola

    >>> suma(1, 20)
    30

    >>> suma(100, 200)
    300

    """
    return numero_1 + numero_2